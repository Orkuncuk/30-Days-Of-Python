print(2 + 3)   # addition(+)[toplama]
print(3 - 1)   # subtraction(-)[çıkarma]
print(2 * 3)   # multiplication(*)[çarpma]
print(3 / 2)   # division(/)[bölme]
print(3 ** 2)  # exponential(**)[üs alma]
print(3 % 2)   # modulus(%)[bölmedeki kalanı veriyor]
print(3 // 2)  # Floor division operator(//)[bölmedeki bölümü veriyor]

# Checking data types

print(type(10))                  # Int[tam sayı]
print(type(3.14))                # Float[doğal sayı]
print(type(1 + 3j))              # Complex[anladığım kadarıyla denklenimsi şeyler]
print(type('Asabeneh'))          # String[metin]
print(type([1, 2, 3]))           # List[liste]
print(type({'name':'Asabeneh'})) # Dictionary[sözlük]
print(type({9.8, 3.14, 2.7}))    # Set[küme]
print(type((9.8, 3.14, 2.7)))    # Tuple[demet]
